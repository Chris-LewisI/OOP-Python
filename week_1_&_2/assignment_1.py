first_int = int(input("First Value: "))
second_int = int(input("Second Value: "))

sum = first_int + second_int
print(f"SUM: {sum}")

difference = first_int - second_int
print(f"DIFFERENCE: {difference}")

product = first_int * second_int
print(f"PRODUCT: {product}")

quotient = first_int / second_int
print(f"QUOTIENT: {quotient}")

remainder = first_int % second_int
print(f"REMAINDER: {remainder}")

exponent_result = first_int ** second_int
print(f"EXPONENT RESULT: {exponent_result}")
